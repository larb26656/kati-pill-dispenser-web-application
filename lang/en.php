	function unit_convert($num,$unit) {
		if($num<=1){
			return $num." ".$unit;
		}
		else{
			return $num." ".$unit."s";
		}
	}
	function unit_convert_with_max_num($num,$maxnum,$unit) {
		if($num<=1){
			return $num."/".$maxnum." ".$unit;
		}
		else{
			return $num."/".$maxnum." ".$unit."s";
		}
	}
